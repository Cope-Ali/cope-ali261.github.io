//hide and unhide example text
function hideText(){
    document.getElementById("exampleText").classList.toggle("hide");
}

// change color from default to blue
function colorText(){
    document.getElementById("exampleText").classList.toggle("color");
}

// move cat from left to right
function moveCat(){
    document.getElementById("cat").classList.toggle("right");
}

//hide and unhide cat
function hideCat(){
    document.getElementById("cat").classList.toggle("hideCat")
}
